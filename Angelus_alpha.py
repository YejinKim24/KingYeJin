def quit():  # 종료 및 메뉴 모듈
    key = pygame.key.get_pressed()
    if key[pygame.K_ESCAPE] == 1:
        quitgame()
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            quitgame()

